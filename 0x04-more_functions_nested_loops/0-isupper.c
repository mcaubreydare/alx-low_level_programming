#include "main.h"
/**
 * _isupper - this is the main function to print lowercase
 * Description: this function is to check for lower case or otherwise
 * @c: is the place holder to test
 * Return: 1 if it is lower and 0 if otherwise
 */
int _isupper(int c)
{

	if (c >= 'A' && c <= 'Z')
	{
	return (1);
	}
	else
	{
	return (0);
	}
}
